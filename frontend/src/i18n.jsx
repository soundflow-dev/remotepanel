const I18nContext = createContext(null)

function lookup(dictionary, key) {
  return key.split(".").reduce((current, part) => current?.[part], dictionary)
}

function interpolate(template, values = {}) {
  return template.replace(/\{(\w+)\}/g, (_, key) => values[key] ?? "")
}

export function LanguageProvider({ children }) {
  const [language, setLanguageState] = useState(() => {
    const saved = window.localStorage.getItem("remotepanel-language")
    return languages.some((item) => item.code === saved) ? saved : "en"
  })

  function setLanguage(nextLanguage) {
    setLanguageState(nextLanguage)
    window.localStorage.setItem("remotepanel-language", nextLanguage)
  }

  const value = useMemo(() => ({
    language,
    setLanguage,
    t(key, values) {
      const template = lookup(translations[language], key) ?? lookup(translations.en, key) ?? key
      return interpolate(template, values)
    },
  }), [language])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const context = useContext(I18nContext)
  if (!context) {
    throw new Error("useI18n must be used inside LanguageProvider")
  }
  return context
}

export function plural(count, suffix = "s") {
  return count === 1 ? "" : suffix
}
